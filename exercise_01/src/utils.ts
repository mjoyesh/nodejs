export const add = (n1: number, n2: number): number => {
  return +n1 + +n2
}

export const subtract = (n1: number, n2: number): number => {
  return n1 - n2
}

export const division = (n1: number, n2: number): number => {
  return n1 / n2
}

export const multiplication = (n1: number, n2: number): number => {
  return n1 * n2
}